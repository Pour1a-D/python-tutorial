# + - * / % -> mod  these are math operators.

num1: int = 10
num2: int = 3
num3: int = -7

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)

print(0.100 + 0.200)


print(max(num1, num2, num3))
print(min(num1, num2, num3))
print(abs(num3))


result = num1 + num3

print("The result is :" + str(result))

print(f"The result is : {result}")
